# ============ 日柱 JDN ============
def jdn(y,m,d):
    a=(14-m)//12; y2=y+4800-a; m2=m+12*a-3
    return d+(153*m2+2)//5+365*y2+y2//4-y2//100+y2//400-32045
SO,BO=9,1

def day_pillar(y,m,d):
    j=jdn(y,m,d)
    return TIANGAN[(j+SO)%10], DIZHI[(j+BO)%12]

# ============ 年柱 ============
def year_pillar(y,m,d):
    lc=LICHUN.get(y,(2,4))
    if m<lc[0] or (m==lc[0] and d<lc[1]): y-=1
    diff=y-1984
    return TIANGAN[diff%10], DIZHI[diff%12]

# ============ 月柱 ============
def get_jieqi_month(y,m,d):
    """
    节气月：1=寅月，2=卯月...12=丑月
    规则：每月的第一个节气日为交节日，此后进入该月
    """
    # 1月：小寒(约5-6日)前=子月，小寒后=丑月
    if m==1:
        xiaohan = JIEQI_FIRST.get(1, 5)
        return 11 if d < xiaohan else 12
    # 2月立春前=丑月，立春后=寅月
    if m==2:
        lc=LICHUN.get(y,(2,4))
        return 12 if (m,d)<lc else 1
    # 其他月份：交日后进入对应节气月（公历月号-1）
    jd=JIEQI_FIRST.get(m,6)
    if d<jd: return m-2  # 交日前，上一个月
    else: return m-1      # 交日后，当前节气月

def month_pillar(year_gan, y,m,d):
    jqm=get_jieqi_month(y,m,d)
    yi=TIAN_IDX[year_gan]
    # 五虎遁：(yi*2 + jqm + 1) % 10
    ms=TIANGAN[(yi*2+jqm+1)%10]
    mz=YUE_ZHI[jqm]
    return ms, mz

